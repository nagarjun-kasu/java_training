package basics;

public class LocalVariables {
	
	public static void main(String[] args) {
		//local variable
		int id=12345;
		
		double d=10.75;
		
		System.out.println(id);
		System.out.println(d);
		
		
	}

}

/*
Variables:
Local Variables: 
Local variables are the variables declared inside the method or constructor or block.
Scope of local variable is within that method or constructor or block only.
Access modifiers can not be given for local variables
There is no default value for local variables.

*/