/*
Inheritance:
It allows one class (child/sub class) to acquire the properties of another class (parent/super class)
 extends

1. Code Reusability:
Reduces duplication of code

2. Method Overriding (Runtime Polymorphism)

3. Improve code maintainability



Syntax:

access_modifier childclassname extends parentclassname{
	---
	--
	--
	--

}

Types of Inheritance:
1. Single Inheritance

class Parent{

}
class Child extends Parent

2. Multilevel Inheritance:

class Parent1{

}

class Child1 extends Parent1{

}

class Child2 extends Child1{

}




3. Hierarchical Inheritance:
class Parent{
}


class child1 extends Parent{

}
class child2 extends Parent{
}
class child3 extends Parent{
}

4. Multiple Inheritance --> Java does not support multiple inheritance

5. Hybrid Inheritance --> it is not supported

*/